    $(function () {
                    
        $('#tree-5f2e89d558a3b').nestable([]);

        $('.tree_branch_delete').click(function() {
            var id = $(this).data('id');
            swal({
                title: "Are you sure to delete this item ?",
                type: "warning",
                showCancelButton: true,
                confirmButtonColor: "#DD6B55",
                confirmButtonText: "Confirm",
                showLoaderOnConfirm: true,
                cancelButtonText: "Cancel",
                preConfirm: function() {
                    return new Promise(function(resolve) {
                        $.ajax({
                            method: 'post',
                            url: 'http://webstudio/public/admin/auth/pagemodule/' + id,
                            data: {
                                _method:'delete',
                                _token:LA.token,
                            },
                            success: function (data) {
                                $.pjax.reload('#pjax-container');
                                toastr.success('Delete succeeded !');
                                resolve(data);
                            }
                        });
                    });
                }
            }).then(function(result) {
                var data = result.value;
                if (typeof data === 'object') {
                    if (data.status) {
                        swal(data.message, '', 'success');
                    } else {
                        swal(data.message, '', 'error');
                    }
                }
            });
        });

        $('.tree-5f2e89d558a3b-save').click(function () {
            var serialize = $('#tree-5f2e89d558a3b').nestable('serialize');

            $.post('http://webstudio/public/admin/auth/pagemodule', {
                _token: LA.token,
                _order: JSON.stringify(serialize)
            },
            function(data){
                $.pjax.reload('#pjax-container');
                toastr.success('Save succeeded !');
            });
        });

        $('.tree-5f2e89d558a3b-refresh').click(function () {
            $.pjax.reload('#pjax-container');
            toastr.success('Refresh succeeded !');
        });

        $('.tree-5f2e89d558a3b-tree-tools').on('click', function(e){
            var action = $(this).data('action');
            if (action === 'expand') {
                $('.dd').nestable('expandAll');
            }
            if (action === 'collapse') {
                $('.dd').nestable('collapseAll');
            }
        });


